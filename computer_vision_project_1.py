def extract_date_from_lines(lines):
    # common date formats: 04/01/2018, 04-01-2018, 2018/01/04 etc.
    date_patterns = [
        r'\b\d{2}[\/\-]\d{2}[\/\-]\d{4}\b',
        r'\b\d{4}[\/\-]\d{2}[\/\-]\d{2}\b'
    ]
    for ln in lines:
        low = ln.lower()
        if "date" in low:
            for pat in date_patterns:
                m = re.search(pat, ln)
                if m:
                    return m.group(0), ln

    for ln in lines:
        for pat in date_patterns:
            m = re.search(pat, ln)
            if m:
                return m.group(0), ln

    return None, None

