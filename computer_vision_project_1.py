def fix_common_ocr_errors(text):
    t = text.upper()


    replacements = {
        "DEIIGHTS": "DELIGHTS",
        "BAK KR": "BAK KUT",
        }

    for wrong, right in replacements.items():
        t = re.sub(rf"\b{re.escape(wrong)}\b", right, t)

    return t

